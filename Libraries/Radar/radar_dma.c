#include "radar_dma.h"


/**********************************************************************************************************
 @Function			void Rader_DMA_Init(void)
 @Description			雷达DMA初始化
 @Input				void
 @Return				void
**********************************************************************************************************/
void Rader_DMA_Init(void)
{
	/* DMA controller clock enable */
	__HAL_RCC_DMA1_CLK_ENABLE();
}

/**********************************************************************************************************
 @Function			void Rader_DMA_DeInit(void)
 @Description			雷达DMA复位
 @Input				void
 @Return				void
**********************************************************************************************************/
void Rader_DMA_DeInit(void)
{
	/* DMA controller clock enable */
	__HAL_RCC_DMA1_CLK_DISABLE();
}

